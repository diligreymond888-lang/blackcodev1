// Function to get unique random entries
export const getRandomUniqueEntries = (count: number): string[] => {
  const shuffled = [...garenaStockData].sort(() => Math.random() - 0.5);
  const uniqueSet = new Set<string>();
  const result: string[] = [];
  
  for (const entry of shuffled) {
    if (!uniqueSet.has(entry) && result.length < count) {
      uniqueSet.add(entry);
      result.push(entry);
    }
    if (result.length >= count) break;
  }
  
  return result;
};
